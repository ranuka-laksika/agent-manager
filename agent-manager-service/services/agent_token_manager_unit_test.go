// -----------------------------------------------------------------------------
// GetJWKS — emits a JWK per loaded public key with the expected metadata.
// -----------------------------------------------------------------------------

func TestAgentTokenManager_GetJWKS(t *testing.T) {
	svc := newTokenManager(t, &clientmocks.OpenChoreoClientMock{})

	jwks, err := svc.GetJWKS(context.Background())

	require.NoError(t, err)
	require.NotNil(t, jwks)
	require.GreaterOrEqual(t, len(jwks.Keys), 1, "at least the active key must be exposed")

	found := false
	for _, k := range jwks.Keys {
		assert.Equal(t, "RSA", k.Kty)
		assert.Equal(t, "sig", k.Use)
		assert.NotEmpty(t, k.N)
		assert.NotEmpty(t, k.E)
		assert.NotEmpty(t, k.Kid)
		if k.Kid == "key-1" {
			found = true
			assert.Equal(t, "RS256", k.Alg)
		}
	}
	assert.True(t, found, "JWKS must include the active key-1")
}
